package com.dreamfirestudios.dreamcore.DreamArmorStand;

/// <summary>
/// Represents the different body parts of an <see cref="org.bukkit.entity.ArmorStand"/>
/// that can be posed or animated.
/// </summary>
public enum ArmorStandPose {
    /// <summary>
    /// The head of the armor stand.
    /// </summary>
    HEAD,

    /// <summary>
    /// The body (torso) of the armor stand.
    /// </summary>
    BODY,

    /// <summary>
    /// The left arm of the armor stand.
    /// </summary>
    LEFT_ARM,

    /// <summary>
    /// The right arm of the armor stand.
    /// </summary>
    RIGHT_ARM,

    /// <summary>
    /// The left leg of the armor stand.
    /// </summary>
    LEFT_LEG,

    /// <summary>
    /// The right leg of the armor stand.
    /// </summary>
    RIGHT_LEG;
}